DATA SEGMENT
	NUM DB 12H
	MES1 DB 10,13,'DATA IS +VE$'
	MES2 DB 10,13,'DATA IS -VE$'
DATA ENDS
CODE SEGMENT
	ASSUME DS:DATA, CS:CODE
	START:	MOV AX,DATA
			MOV DS,AX
			MOV AL,NUM
			ROL AL,1
			JC NEGA
			MOV DX, OFFSET MES1
			JMP EXIT
			NEGA : 
				MOV DX, OFFSET MES2
			EXIT:
				MOV AH,09H
				INT 21H
				MOV AH,4CH
			INT 21H	
CODE ENDS
END START	