DATA SEGMENT
	NUM DW 26
	MES1 DB 'EVEN$'
	MES2 DB 'ODD$'
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE, DS:DATA
	START:
		MOV AX,DATA
		MOV DS,AX
		MOV AX,NUM
		MOV BX,02
		DIV BX
		CMP DX,00
		JNZ NEGA
		MOV DX,OFFSET MES1
		JMP EXIT
		NEGA : 
			MOV DX,OFFSET MES2
		EXIT:
			MOV AH,09H
			INT 21H
			MOV AH,4CH
			INT 21H	
CODE ENDS
END START