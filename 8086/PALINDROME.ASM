DATA SEGMENT
S1 DB 'MAYAMI$'
LEN EQU $-S1
S2 DB 20 DUP(0)
M1 DB 'PALINDROME$'
M2 DB 'NOT PALINDROME$'

DATA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:MOV AX,DATA
MOV DS,AX

MOV ES,AX
LEA SI,S1
LEA DI,S2+LEN-1
MOV CX,LEN
UP:	CLD
	LODSB
	STD
	STOSB
   LOOP UP
LEA SI,S1
LEA DI,S2
CLD
MOV CX,LEN
REPNE CMPSB

CMP CX,0H

JNZ NOTPALIN

LEA DX,M1
MOV AH,09H
INT 21H
JMP EXIT

NOTPALIN:LEA DX,M2
MOV AH,09H
INT 21H

EXIT:
MOV AH,4CH
INT 21H
CODE ENDS
END START
